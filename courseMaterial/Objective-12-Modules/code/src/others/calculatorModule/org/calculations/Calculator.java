package org.calculations;

public class Calculator{
	public float add(float x, float y){System.out.println("Add");return x+y;}
	public float subtract(float x, float y){System.out.println("Subtract");return x-y;}
	public float multiply(float x, float y){System.out.println("Multiply");return x*y;}
	public float divide(float x, float y){System.out.println("Divide");return x/y;}
}